function solution(arr){
    let answer = '';
    let box = new Map();
    let max = Number.MIN_SAFE_INTEGER;

    for(let x of arr){
        if(box.has(x)) box.set(x,box.get(x)+1)
        else box.set(x,1)
    }
    for(let [key,val] of box){
        if(val>max){
            max=val
            answer=key
        }
    }
    return answer;
}

